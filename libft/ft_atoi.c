#include "libft.h"

int	ft_atoi(const char *s)
{
	long	res;
	int		sign;

	while ((*s >= 9 && *s <= 13) || *s == 32)
		s++;
	sign = 1;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	res = 0;
	while (*s >= '0' && *s <= '9')
	{
		if (res > (LONG_MAX - (*s - '0')) / 10)
			return (-sign * (sign == 1));
		res = (res * 10) + (*s - '0');
		s++;
	}
	return ((int)res * sign);
}
